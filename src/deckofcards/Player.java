package deckofcards;
 public class Player {
        private String name;
        private Card[] hand;

        // Constructor
        public Player(String name) {
            this.name = name;
            this.hand = hand;
        }

        // Getters
        public String getName() {
            return name;
        }

        public Card[] getHand() {
            return hand;
        }

}
